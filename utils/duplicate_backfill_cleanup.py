async def get_duplicate_verifications(duplicate_data, verifications_in_use):
    """
    returns a dic:
    key: (user_id, oe_id)
    value: set of verification ids to delete
    """

    verification_ids = {}  # A dict: (user_id, oe_id) -> verification_id

    # get all verification ids mapping to (user_id, oe_id) pair
    for user_id, _, oe_id in duplicate_data:
        verification_ids[(user_id, oe_id)] = set()
        async with member_client.client.connector.transaction() as con:
            rows = await con.fetch(
                "select id from eligibility.verification where user_id = $1 and (additional_fields #>> '{}')::jsonb -> 'organization_employee_id' = $2",
                user_id,
                oe_id,
            )
            for r in rows:
                verification_ids[(user_id, oe_id)].add(r["id"])

    # remove the verification id if in use or just the first one
    for user_id, _, oe_id in duplicate_data:
        if (user_id, oe_id) in verifications_in_use:
            verification_ids[(user_id, oe_id)].remove(
                verifications_in_use[(user_id, oe_id)]
            )
        else:
            verification_ids[(user_id, oe_id)].pop()

    return verification_ids


async def delete_duplicate_backfill_verifications(duplicate_data, verifications_in_use):
    verification_dict = await get_duplicate_verifications(
        duplicate_data, verifications_in_use
    )

    for user_id, oe_id in verification_dict:
        deleted_count = 0
        logger.info(f"remove duplicate backfill for user_id={user_id}, oe_id={oe_id}")
        async with member_client.client.connector.transaction() as con:
            for id in verification_dict[(user_id, oe_id)]:
                await con.execute(
                    "delete from eligibility.member_verification where verification_id = $1",
                    id,
                )
                await con.execute(
                    "delete from eligibility.verification where id = $1", id
                )
                deleted_count += 1

        logger.info(
            f"remove duplicate backfill for user_id={user_id}, oe_id={oe_id}, {deleted_count} verifications deleted"
        )


